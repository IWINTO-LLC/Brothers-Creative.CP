  /// تنسيق رقم الهاتف للعرض
  static String formatPhoneForDisplay(String phoneNumber) {
    // إزالة المسافات والرموز الخاصة
    final String cleanPhone = phoneNumber.replaceAll(RegExp(r'[\s\-\(\)]'), '');

    // إذا كان الرقم يبدأ بـ +966، اعرضه بصيغة سعودية
    if (cleanPhone.startsWith('+966')) {
      final localNumber = cleanPhone.substring(4);
      if (localNumber.length == 9 && localNumber.startsWith('5')) {
        return '0$localNumber';
      }
    }

    // إذا كان الرقم يبدأ بـ 966، اعرضه بصيغة سعودية
    if (cleanPhone.startsWith('966')) {
      final String localNumber = cleanPhone.substring(3);
      if (localNumber.length == 9 && localNumber.startsWith('5')) {
        return '0$localNumber';
      }
    }

    // إذا كان الرقم يبدأ بـ 0، اتركه كما هو
    if (cleanPhone.startsWith('0')) {
      return cleanPhone;
    }

    // إذا كان الرقم يبدأ بـ 5، أضف 0
    if (cleanPhone.startsWith('5') && cleanPhone.length == 9) {
      return '0$cleanPhone';
    }

    return cleanPhone;
  }

