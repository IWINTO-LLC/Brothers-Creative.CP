  static String? validateEmail(String? value) {
    if (value == null || value.isEmpty) {
      return 'Email is required.';
    }

    // التحقق من تنسيق البريد الإلكتروني
    final emailRegExp = RegExp(r'^[\w-\.]+@([\w-]+\.)+[\w-]{2,4}$');
    if (!emailRegExp.hasMatch(value)) {
      return 'Invalid email address.';
    }

    return null;
  }

  static String? validatePassword(String? value) {
    if (value == null || value.isEmpty) {
      return 'Password is required.';
    }

    // التحقق من طول كلمة المرور
    if (value.length < 6) {
      return 'Password must be at least 6 characters long.';
    }

    return null;
  }

  /// التحقق من صحة رقم الهاتف وتنسيقه
  static String? validatePhoneNumber(String? value) {
    if (value == null || value.isEmpty) {
      return 'رقم الهاتف مطلوب';
    }

    // إزالة المسافات والرموز الخاصة
    final cleanPhone = value.replaceAll(RegExp(r'[\s\-\(\)]'), '');

    // التحقق من أن الرقم يحتوي على أرقام فقط (بعد إزالة +)
    final phoneDigits =
        cleanPhone.startsWith('+') ? cleanPhone.substring(1) : cleanPhone;

    if (!RegExp(r'^\d+$').hasMatch(phoneDigits)) {
      return 'رقم الهاتف يجب أن يحتوي على أرقام فقط';
    }

    // التحقق من طول الرقم (7-15 رقم)
    if (phoneDigits.length < 7 || phoneDigits.length > 15) {
      return 'رقم الهاتف يجب أن يكون بين 7 و 15 رقم';
    }

    return null;
  }

  /// تنسيق رقم الهاتف للاستخدام مع Firebase
  static String formatPhoneForFirebase(String phoneNumber,
      {String? countryCode}) {
    // إزالة المسافات والرموز الخاصة
    String cleanPhone = phoneNumber.replaceAll(RegExp(r'[\s\-\(\)]'), '');

    // إذا كان الرقم يبدأ بـ +، اتركه كما هو
    if (cleanPhone.startsWith('+')) {
      return cleanPhone;
    }

    // إذا تم توفير رمز الدولة، استخدمه
    if (countryCode != null && countryCode.isNotEmpty) {
      // إزالة الصفر من البداية إذا كان موجوداً
      if (cleanPhone.startsWith('0')) {
        cleanPhone = cleanPhone.substring(1);
      }
      return '+$countryCode$cleanPhone';
    }

    // محاولة اكتشاف رمز الدولة تلقائياً
    final String detectedCountryCode = _detectCountryCode(cleanPhone);
    if (detectedCountryCode.isNotEmpty) {
      return '+$detectedCountryCode$cleanPhone';
    }

    // إذا لم يتم العثور على رمز دولة، استخدم السعودية كافتراضي
    if (cleanPhone.startsWith('0')) {
      return '+966${cleanPhone.substring(1)}';
    }

    return '+966$cleanPhone';
  }

